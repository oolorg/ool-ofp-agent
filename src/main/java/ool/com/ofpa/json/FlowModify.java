package ool.com.ofpa.json;

public class FlowModify {
	private String ofcUrl;
	private String type;
	private String ip;
	private String inPort;
	private String outPort;
	public String getOfcUrl() {
		return ofcUrl;
	}
	public void setOfcUrl(final String ofcUrl) {
		this.ofcUrl = ofcUrl;
	}
	public String getType() {
		return type;
	}
	public void setType(final String type) {
		this.type = type;
	}
	public String getIp() {
		return ip;
	}
	public void setIp(final String ip) {
		this.ip = ip;
	}
	public String getInPort() {
		return inPort;
	}
	public void setInPort(final String inPort) {
		this.inPort = inPort;
	}
	public String getOutPort() {
		return outPort;
	}
	public void setOutPort(final String outPort) {
		this.outPort = outPort;
	}
}
